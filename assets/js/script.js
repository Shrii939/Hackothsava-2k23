// Set the date to countdown to
var countDownDate = new Date("June 2, 2023 00:00:00").getTime();

// Update the count down every 1 second
var x = setInterval(function() {

  // Get the current date and time
  var now = new Date().getTime();

  // Calculate the distance between now and the countdown date
  var distance = countDownDate - now;

  // Calculate days, hours, minutes, and seconds remaining
  var days = Math.floor(distance / (1000 * 60 * 60 * 24));
  var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
  var seconds = Math.floor((distance % (1000 * 60)) / 1000);

  // Update the HTML with the remaining time
  document.getElementById("day_time_counter").innerHTML =
    "<div class='days'>" + days + " Days</div>" +
    "<div class='hours'>" + hours + " Hours</div>" +
    "<div class='minutes'>" + minutes + " Minutes</div>" +
    "<div class='seconds'>" + seconds + " Seconds</div>";

  // If the countdown is finished, display a message
  if (distance < 0) {
    clearInterval(x);
    document.getElementById("day_time_counter").innerHTML = "EXPIRED";
  }
}, 1000);

